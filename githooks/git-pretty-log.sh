#!/bin/bash

set -e
cd $1
if [ "$3" == '0000000000000000000000000000000000000000' ]; then
    return ''
fi
if [ "$2" == '0000000000000000000000000000000000000000' ]; then
    git log -100 --pretty='%h______%p______%t______%an______%cn______%ct______%ce______%ae______%s' $3
else
    git log -100 --pretty='%h______%p______%t______%an______%cn______%ct______%ce______%ae______%s' $2..$3
fi
